/*---INTRODUZINDO SIMETRIA ELIPTICA---*****************************************/
double CSI2(double X, double Y, double q)           {return sqrt(q*X*X + Y*Y/q);}
double CSI2U(double X, double Y, double q, double u){return sqrt(u*( X*X/( 1.0 - (1.0-(1.0/q))*u ) + Y*Y/( 1.0 - (1.0 - q)*u ) ));}

double kCSI2 (double X, double Y, double q, double ks)          {return k( CSI2(X,Y,q)  ,ks);}
double kCSI2U(double X, double Y, double q, double ks, double u){return k(CSI2U(X,Y,q,u),ks);}
double klinhaCSI2U(double X, double Y, double q, double ks, double u)
{return klinha(CSI2U(X,Y,q,u),ks);}
/******************************************************************************/


/*---DEFININDO OS ARGUMENTOS DAS INTEGRAIS---**********************************/
double AJ(int n, double X, double Y, double q, double ks, double u)
{return        kCSI2U(X,Y,q,ks,u)/( pow(1.0 - (1.0-q)*u , double(n) + 0.5)*pow(1.0 - (1.0-(1.0/q))*u ,1.5 - double(n)) );}

double AK(int n, double X, double Y, double q, double ks, double u)
{return u*klinhaCSI2U(X,Y,q,ks,u)/( pow(1.0 - (1.0-q)*u , double(n) + 0.5)*pow(1.0 - (1.0-(1.0/q))*u ,2.5 - double(n)) );}
